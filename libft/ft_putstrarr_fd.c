#include "../includes/libft.h"

void	ft_putstrarr_fd(char **s, int fd)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != NULL)
		{
			ft_putstr_fd(s[i], fd);
			i++;
		}
	}
}
