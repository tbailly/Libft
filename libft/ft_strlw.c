#include "../includes/libft.h"

char	*ft_strlw(char *s)
{
	unsigned	int	i;

	if (s == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		s[i] = ft_tolower(s[i]);
		i++;
	}
	return (s);
}
