#include "../includes/libft.h"

void	ft_putstrarr(char **s)
{
	int	i;

	i = 0;
	if (s != NULL)
	{
		while (s[i] != NULL)
		{
			ft_putstr(s[i]);
			i++;
		}
	}
}
